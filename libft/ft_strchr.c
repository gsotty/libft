#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	x;

	x = 0;
	while (x <= ft_strlen(s))
	{
		if (s[x] == c)
		{
			return ((char *)s + x);
		}
		x++;
	}
	return (NULL);
}
